public class ternary_operator {
    public static void main(String[] args) {

        // Ternary Operators

        // variale = (condition)? expressionTrue : expressionFalse;

        int time = 20;
        String result;
        
        result = (time<12)? "Good Morning. " : "Good Evening. " ;
        System.out.println(result);
        

        int age=18;
        String answer;

        answer = (age>=18)? "You can drive." : "You can not drive." ;
        System.out.println(answer);


        int f=56;
        String answer1;

        answer1=(f<=56)? "it is true":"it is false";
        System.out.println(answer1);





















    }
    
}
